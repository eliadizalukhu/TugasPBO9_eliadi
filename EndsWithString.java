public class EndsWithString {
    public static void main(String[] args) {
        String string1 = "Hello Eliadi";
        System.out.println("Hasilnya adalah:" + string1.endsWith("e"));
        System.out.println("Hasilnya adalah:" + string1.endsWith("d"));
        System.out.println("Hasilnya adalah:" + string1.endsWith("Hello"));
        System.out.println("Hasilnya adalah:" + string1.endsWith("Eliadi"));
    }

}
